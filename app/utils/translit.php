<?php
/**
 * Класс для перевода кириллицы в транслит
 */
class Translit {
    /**
     *
     * @var array Массив соответствий букв кириллицы к транслиту
     */
    private $tr = [
        "А"=>"A","Б"=>"B","В"=>"V","Г"=>"G","Д"=>"D",
        "Е"=>"E","Ё"=>"YO","Ж"=>"J","З"=>"Z","И"=>"I",
        "Й"=>"Y","К"=>"K","Л"=>"L","М"=>"M","Н"=>"N",
        "О"=>"O","П"=>"P","Р"=>"R","С"=>"S","Т"=>"T",
        "У"=>"U","Ф"=>"F","Х"=>"H","Ц"=>"C","Ч"=>"CH",
        "Ш"=>"SH","Щ"=>"SCH","Ъ"=>"","Ы"=>"YI","Ь"=>"",
        "Э"=>"E","Ю"=>"YU","Я"=>"YA","а"=>"a","б"=>"b",
        "в"=>"v","г"=>"g","д"=>"d","е"=>"e","ё"=>"yo","ж"=>"j",
        "з"=>"z","и"=>"i","й"=>"y","к"=>"k","л"=>"l",
        "м"=>"m","н"=>"n","о"=>"o","п"=>"p","р"=>"r",
        "с"=>"s","т"=>"t","у"=>"u","ф"=>"f","х"=>"h",
        "ц"=>"c","ч"=>"ch","ш"=>"sh","щ"=>"sch","ъ"=>"y",
        "ы"=>"y","ь"=>"","э"=>"e","ю"=>"yu","я"=>"ya"
    ];

    /**
     * Переводит кириллицу в транслит
     * @param string $string Исходная строка на кириллице
     * @return string Транслит
     */
    public function cyrToLat($string){
        return strtr($string, $this->tr);
    }
    
    /**
     * @param string $string Проверка является ли строка кириллицей
     * @return boolean true - кириллица, false - не кириллица 
     */
    public static function itCyrillic($string){
        return preg_match('^[а-яА-ЯёЁ0-9]+$^', $string);
    }
    
    public function splitNameSurname($string){
        return explode(' ', $string);
    }
}

 